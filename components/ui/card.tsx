import * as React from "react";

import { cn } from "@/lib/utils";

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: "default" | "brand" | "brandOutline" | "brandGradient";
  }
>(({ className, variant = "default", ...props }, ref) => {
  const variantClasses = {
    default: "rounded-xl border bg-card text-card-foreground shadow",
    brand:
      "rounded-xl border border-brand-50/30 bg-white/10 backdrop-blur-sm shadow-lg",
    brandOutline:
      "rounded-xl border-2 border-brand-50/50 bg-white shadow-lg hover:shadow-xl",
    brandGradient:
      "rounded-xl border border-brand-50/20 bg-gradient-to-br from-brand-50/5 to-brand-400/5 shadow-lg",
  };

  return (
    <div
      ref={ref}
      className={cn(
        variantClasses[variant],
        "transition-all duration-300",
        className
      )}
      {...props}
    />
  );
});
Card.displayName = "Card";

